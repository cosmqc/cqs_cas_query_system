package seng202.team9.models;

/**
 * Holds the different possible modals that can be open
 */
public enum ModalType {
    /**
     * Represents a modal for loading data.
     */
    LOAD_DATA,

    /**
     * Represents a modal for performing queries.
     */
    QUERY,

    /**
     * Represents no active modal.
     */
    NONE
}
