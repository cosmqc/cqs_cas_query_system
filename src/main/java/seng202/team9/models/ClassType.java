package seng202.team9.models;

/**
 * Simple enum so the type the value <b>should</b> be can be encoded.
 * Probably a better way to do this, but this is easier to read than massive amounts of .class comparisons
 *
 * @author Jake Dalton
 */
public enum ClassType {


    /**
     * Represents an integer data type.
     */
    INT,

    /**
     * Represents a string data type.
     */
    STRING,

    /**
     * Represents a floating-point data type.
     */
    FLOAT
}
